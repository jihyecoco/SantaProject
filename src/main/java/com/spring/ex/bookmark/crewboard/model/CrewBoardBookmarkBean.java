package com.spring.ex.bookmark.crewboard.model;

public class CrewBoardBookmarkBean {
		
    private int bookmarkId;
	private int crewboardNum;
	private String userId;
	private String bookmarkDate;
	
	//getter & setter
	public int getBookmarkId() {
		return bookmarkId;
	}
	public void setBookmarkId(int bookmarkId) {
		this.bookmarkId = bookmarkId;
	}
	public int getCrewboardNum() {
		return crewboardNum;
	}
	public void setCrewboardNum(int crewboardNum) {
		this.crewboardNum = crewboardNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getBookmarkDate() {
		return bookmarkDate;
	}
	public void setBookmarkDate(String bookmarkDate) {
		this.bookmarkDate = bookmarkDate;
	}
	
		
}//CrewBoardBookmarkBean
